#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    /**
     * double loop.........O(n^2).
     */
    for (size_t i = 0; i < n; i++)
    {
        for (size_t i = 0; i < n; i++)
        {
            /* code */
        }
        
    }

    /**
     * Triple loop.......O(n^3).
     */
    for (size_t i = 0; i < n; i++)
    {
        for (size_t i = 0; i < n; i++)
        {
            for (size_t i = 0; i < n; i++)
            {
                /* code */
            }
            
        }
        
    }
    
    
    return 0;
}